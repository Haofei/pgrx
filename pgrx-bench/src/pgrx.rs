#[doc(hidden)]
/// Runtime hooks supplied by pgrx-generated wrappers for Postgres exception and subtransaction
/// boundaries.
pub trait Runtime {
    fn execute_guarded<F, T>(&self, f: F) -> Result<T, String>
    where
        F: FnOnce() -> Result<T, String>;

    fn with_subtransaction<F, T>(&self, f: F) -> Result<T, String>
    where
        F: FnOnce() -> T;
}

#[doc(hidden)]
/// Internal entrypoint used by generated `#[pg_bench]` wrappers.
pub fn execute_benchmark<R: Runtime>(
    definition: BenchDefinition,
    setup_fn: Option<fn()>,
    bench_fn: fn(&mut Bencher<'_>),
    baseline_artifacts: Option<Value>,
    runtime: &R,
) -> Value {
    let definition_for_report = definition.clone();
    let result = runtime.execute_guarded(|| {
        let baseline_artifacts = baseline_artifacts
            .map(|baseline_artifacts| {
                serde_json::from_value::<Vec<BenchArtifact>>(baseline_artifacts).map_err(|error| {
                    format!("failed to decode persisted Criterion baseline artifacts: {error}")
                })
            })
            .transpose()?;

        if let Some(setup_fn) = setup_fn {
            setup_fn();
        }

        let mut bencher = Bencher::new(definition.transaction_mode);
        bench_fn(&mut bencher);
        let routine = bencher.into_routine()?;

        let tempdir = TempDir::new().map_err(|e| e.to_string())?;
        // Recreate Criterion's `<bench>/base/*.json` layout up front so the benchmark run and its
        // comparison analysis are driven by Criterion's own baseline machinery rather than by
        // cargo-pgrx trying to infer a comparison from normalized tables after the fact.
        if let Some(baseline_artifacts) = baseline_artifacts.as_deref() {
            crate::materialize_baseline_artifacts(tempdir.path(), baseline_artifacts)?;
        }

        let mut criterion = crate::build_criterion(
            &definition.config,
            tempdir.path(),
            baseline_artifacts.is_some(),
        );
        let mut routine = routine;

        criterion.bench_function(definition.bench_name, |criterion_bencher| {
            crate::run_routine(
                criterion_bencher,
                &mut routine,
                definition.transaction_mode,
                runtime,
            );
        });
        criterion.final_summary();

        let report = crate::parse_benchmark_output(
            definition.clone(),
            tempdir.path(),
            baseline_artifacts.as_deref(),
        )?;
        Ok::<BenchResult, String>(report)
    });

    match result {
        Ok(report) => serde_json::to_value(report).expect("BenchResult should serialize"),
        Err(error_text) => serde_json::to_value(BenchResult {
            schema_name: definition_for_report.schema_name.to_string(),
            bench_name: definition_for_report.bench_name.to_string(),
            function_name: definition_for_report.function_name.to_string(),
            setup_function: definition_for_report.setup_function.map(str::to_string),
            transaction_mode: definition_for_report.transaction_mode,
            source_file: definition_for_report.source_file.to_string(),
            source_line: definition_for_report.source_line,
            criterion_config: definition_for_report.config,
            status: BenchStatus::Failed,
            error_text: Some(error_text),
            benchmark: None,
            estimates: Vec::new(),
            samples: Vec::new(),
            throughput: None,
            comparison: None,
            artifacts: Vec::new(),
        })
        .expect("BenchResult should serialize"),
    }
}

#[doc(hidden)]
/// Internal entrypoint used by generated `#[pg_bench]` descriptor wrappers.
pub fn describe_benchmark(definition: BenchDefinition) -> Value {
    serde_json::to_value(BenchDescriptor {
        schema_name: definition.schema_name.to_string(),
        bench_name: definition.bench_name.to_string(),
        function_name: definition.function_name.to_string(),
        setup_function: definition.setup_function.map(str::to_string),
        transaction_mode: definition.transaction_mode,
        source_file: definition.source_file.to_string(),
        source_line: definition.source_line,
        criterion_config: definition.config,
    })
    .expect("BenchDescriptor should serialize")
}

#[doc(hidden)]
/// Internal helper used by the macro crate to enforce `mod benches`.
pub const fn module_path_has_benches(module_path: &str) -> bool {
    crate::ends_with(module_path.as_bytes(), b"::benches")
        || crate::equals(module_path.as_bytes(), b"benches")
}
