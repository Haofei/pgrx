//! Internal support API for pgrx-generated benchmark wrappers and `cargo pgrx bench`.
//!
//! Benchmark authors should use the top-level [`crate::Bencher`], [`crate::BatchSize`], and
//! [`crate::black_box`] APIs instead of the items in this module.
//!
//! These helpers intentionally exchange plain `serde_json::Value` payloads with the generated
//! wrappers so `pgrx-bench` can stay free of direct `pgrx-*` crate dependencies; the
//! `pgrx::JsonB`, `PgTryBuilder`, and subtransaction boundaries stay in the macro-generated SQL
//! wrapper layer.

use serde::{Deserialize, Serialize};
use serde_json::Value;
use tempfile::TempDir;

use crate::Bencher;

#[derive(Debug, Clone, Copy, Serialize, Deserialize, Eq, PartialEq)]
#[serde(rename_all = "snake_case")]
#[doc(hidden)]
/// Internal transaction metadata generated by `#[pg_bench]`.
pub enum TransactionMode {
    Shared,
    SubtransactionPerBatch,
    SubtransactionPerIteration,
}

impl TransactionMode {
    #[doc(hidden)]
    /// Returns the SQL/JSON representation used by cargo-pgrx.
    pub fn as_str(self) -> &'static str {
        match self {
            Self::Shared => "shared",
            Self::SubtransactionPerBatch => "subtransaction_per_batch",
            Self::SubtransactionPerIteration => "subtransaction_per_iteration",
        }
    }
}

#[derive(Debug, Clone, Serialize, Deserialize)]
#[doc(hidden)]
/// Internal Criterion configuration generated by `#[pg_bench]`.
pub struct BenchConfig {
    pub sample_size: usize,
    pub measurement_time_ms: u64,
    pub warm_up_time_ms: u64,
    pub nresamples: usize,
    pub noise_threshold: f64,
    pub significance_level: f64,
}

impl Default for BenchConfig {
    fn default() -> Self {
        Self {
            sample_size: crate::DEFAULT_SAMPLE_SIZE,
            measurement_time_ms: crate::DEFAULT_MEASUREMENT_TIME_MS,
            warm_up_time_ms: crate::DEFAULT_WARM_UP_TIME_MS,
            nresamples: crate::DEFAULT_NRESAMPLES,
            noise_threshold: crate::DEFAULT_NOISE_THRESHOLD,
            significance_level: crate::DEFAULT_SIGNIFICANCE_LEVEL,
        }
    }
}

#[derive(Debug, Clone, Serialize, Deserialize)]
#[doc(hidden)]
/// Internal benchmark definition generated by `#[pg_bench]`.
pub struct BenchDefinition {
    pub schema_name: &'static str,
    pub bench_name: &'static str,
    pub function_name: &'static str,
    pub setup_function: Option<&'static str>,
    pub transaction_mode: TransactionMode,
    pub source_file: &'static str,
    pub source_line: u32,
    pub config: BenchConfig,
}

#[derive(Debug, Clone, Serialize, Deserialize)]
#[doc(hidden)]
/// Internal benchmark result payload returned from the backend to `cargo pgrx bench`.
pub struct BenchResult {
    pub schema_name: String,
    pub bench_name: String,
    pub function_name: String,
    pub setup_function: Option<String>,
    pub transaction_mode: TransactionMode,
    pub source_file: String,
    pub source_line: u32,
    pub criterion_config: BenchConfig,
    pub status: BenchStatus,
    pub error_text: Option<String>,
    pub benchmark: Option<CriterionBenchmark>,
    pub estimates: Vec<BenchEstimate>,
    pub samples: Vec<BenchSample>,
    pub throughput: Option<BenchThroughput>,
    pub comparison: Option<BenchComparison>,
    #[serde(default, skip_serializing_if = "Vec::is_empty")]
    pub artifacts: Vec<BenchArtifact>,
}

